    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        DefaultTableModel tabela = (DefaultTableModel) jTableEditoras.getModel();
        cEditoras controllerEditoras = new cEditoras();
        tabela.setNumRows(0);

        for (mEditoras modelEditoras : controllerEditoras.pesquisar(jTextBusca.getText(), jComboFiltro.getSelectedIndex())) {
            tabela.addRow(new Object[]{
                modelEditoras.getId_editora(),
                modelEditoras.getNome()
            });

        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        mEditoras modelEditoras = new mEditoras();
        cEditoras controllerEditoras = new cEditoras();

        modelEditoras.setId_editora(Integer.parseInt(jTextId.getText()));
        modelEditoras.setNome(jTextNome.getText());

        controllerEditoras.alterarEditoras(modelEditoras);
        listarDados();


    }//GEN-LAST:event_jButtonAlterarActionPerformed

