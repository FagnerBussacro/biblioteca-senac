        cEditoras controlerEditoras = new cEditoras();

        modelEditoras.setNome(jTextNome.getText());
        controlerEditoras.cadastrarEditoras(modelEditoras);
        listarDados();

        jTextNome.setText("");
        jTextId.setText("");


